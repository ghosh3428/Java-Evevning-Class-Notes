class FirstExample
{
    public static void main(String [] args)
    {
        int first = 10;
        int second = 3;

        System.out.println("10 + 3 = " + (first + second));
        System.out.println("10 - 3 = " + (first - second));
        System.out.println("10 * 3 = " + (first * second));
        System.out.println("10 / 3 = " + (first / second));
        System.out.println("10 % 3 = " + (first % second));
    }
}